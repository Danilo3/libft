#include "libft.h"

void	ft_print_sqmt(char **mt)
{
	size_t i;

	i = 0;
	while (mt[i])
	{
		ft_putstr(mt[i++]);
		if (mt[i] && mt[i][0])
			ft_putchar('\n');
	}
}
